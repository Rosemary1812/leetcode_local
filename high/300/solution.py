# ===== 300. 最长上升子序列 =====
# 难度: 中等
# 英文名: Longest Increasing Subsequence
# 来源: https://leetcode.cn/problems/longest-increasing-subsequence/description/
#
# 给你一个整数数组 nums ，找到其中最长严格递增子序列的长度。
#
# 子序列是由数组派生而来的序列，删除（或不删除）一些元素而不改变其余元素的顺序。
#
# 示例 1：
# 输入：nums = [10,9,2,5,3,7,101,18]
# 输出：4
# 解释：最长递增子序列是 [2,3,7,101]，所以长度是 4。
#
# 示例 2：
# 输入：nums = [0,1,0,3,2,3]
# 输出：4
#
# 示例 3：
# 输入：nums = [7,7,7,7,7,7,7]
# 输出：1
#
# --------------------------------------------------
def lengthOfLIS(nums):
    f = [0] * len(nums)
    for i, x in enumerate(nums):
        for j, y in enumerate(nums[:i]):
            if x > y:
                f[i] = max(f[i], f[j])
        f[i] += 1
    return max(f)


print(lengthOfLIS([10, 9, 2, 5, 3, 7, 101, 18]))  # 4
print(lengthOfLIS([0, 1, 0, 3, 2, 3]))  # 4
print(lengthOfLIS([7, 7, 7, 7, 7, 7, 7]))  # 1


# ● 最长递增子序列（ LIS ）—
#   动态规划

#   核心定义

#   f[i] = 以 nums[i]
#   结尾的最长递增子序列长度

#   状态转移

#   对于每个位置
#   i，往前找所有比它小的 j：

#   f[i] = max(f[j]) + 1  (其中 j
#   < i 且 nums[j] < nums[i])

#   即：以 nums[i]
#   结尾的最长递增子序列 =
#   前面所有小于 nums[i]
#   的元素中，f 值最大的那个 +1。

#   执行流程（以 [10, 9, 2, 5, 3,
#   7, 101, 18] 为例）

#   i=0, x=10  →
#   没有更小的元素在前面  → f[0]=1
#       [10]
#   i=1, x=9   → 没有更小的元素
#        → f[1]=1    [9]
#   i=2, x=2   → 没有更小的元素
#        → f[2]=1    [2]
#   i=3, x=5   → 前面<5的有 2
#        → f[3]=f[2]+1=2  [2,5]
#   i=4, x=3   → 前面<3的有 2
#        → f[4]=f[2]+1=2  [2,3]
#   i=5, x=7   → 前面<7的有 2,5,3
#        → f[5]=max(f[3],f[4])+1=3
#     [2,5,7]或[2,3,7]
#   i=6, x=101 → 前面全部小于101
#       → f[6]=max(all f)+1=4
#   [2,5,7,101]
#   i=7, x=18  → 前面<18的有
#   2,5,3,7  →
#   f[7]=max(1,2,2,3)+1=4
#   [2,5,7,18]

#   最终 max(f) = 4，答案就是 4。

#   复杂度

#   ┌─────────┬───────┬──────┐
#   │         │ 时间  │ 空间 │
#   ├─────────┼───────┼──────┤
#   │ 暴力 DP │ O(n²) │ O(n) │
#   └─────────┴───────┴──────┘

#   两层循环：外层遍历每个位置，内
#   层往前找所有更小的元素。

#   为什么返回 max(f) 而不是
#   f[-1]？

#   因为递增子序列不一定以最后一个
#   元素结尾，所以取全局最大值。
